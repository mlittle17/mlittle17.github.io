// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function min(num1, num2) {
  if(num1 < num2) {
    return num1
  } else return num2

}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(number) {

  if(number % 2 === 0) {
      return true
    } else return false

}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string, char) {
  let counter = 0;
  for (let i = 0; i < string.length; i++) {
    if(string[i] === char) {
      counter += 1
    }
  } return counter

}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(string) {
  let counter = 0
  for(let i = 0; i < string.length; i++) {
    
    if(string[i] === "B") {
      counter += 1
    }
  } return counter;

}

